import numpy as np
a=np.array(["akash","rathore","course"])
b=" ".join(a)
#capitalize
print(np.array(b.capitalize().split()))
#converted into lowercase
print(np.array(b.lower().split()))
#converyted into uppercase
print(np.array(b.upper().split()))
#converted into title case
print(np.array(b.title().split()))
#converted into swapcase
print(np.array(b.swapcase().split()))
